 ## App Exercise - Percent Completed
 
 >Ces exercices renforcent les concepts Swift dans le contexte d'une application de suivi de la condition physique.
   
   Vous décidez que votre application de suivi de la condition physique doit indiquer à l'utilisateur quel pourcentage de son objectif a été atteint jusqu'à présent. Déclarez une variable appelée `percentCompleted` et définissez-la sur 0. Ne lui attribuez pas explicitement un type.
 */


/*:
Imaginez qu'en cours de journée, un utilisateur ait franchi 3 467 étapes sur l'objectif de 10 000 étapes. Cela signifie qu'il / elle est 34,67% du chemin à son objectif. Assignez 34,67 à `percentCompleted`. Le code est-il compilé? Revenir en arrière et explicitement assigner un type à `percentCompleted` qui permettra au code de se compiler.
 */


