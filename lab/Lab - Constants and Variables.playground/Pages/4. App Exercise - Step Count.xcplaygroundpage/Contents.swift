/*:
 ## App Exercise - Step Count
 
 >Ces exercices renforcent les concepts Swift dans le contexte d'une application de suivi de la condition physique.
 
Créez une variable appelée `steps` qui gardera trace du nombre de pas que vous ferez tout au long de la journée. Définissez sa valeur initiale sur 0 pour représenter le nombre de pas en tout début de matinée. Imprimez `steps` sur la console.
